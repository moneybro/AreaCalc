using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AreaCalc
{
    public class RectangleArea
    {
        /// <summary>
        /// Расчет площади прямоугольника
        /// </summary>
        /// <param name="a"></param>
        /// <param name="b"></param>
        /// <returns></returns>
        public static double Calc(double a, double b)
        {
            double res = a * b;
            return res;
        }
    }
